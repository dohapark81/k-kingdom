for (const dynasty of dynasties) {
  for (const king of dynasty.kings) {
    if (birthYears[king.id]) {
      king.birthYear = birthYears[king.id];
    }
    if (successions[king.id]) {
      king.succession = successions[king.id];
    }
  }
}

export function getDynasty(id: string): Dynasty | undefined {
  return dynasties.find((d) => d.id === id);
}

export function getKing(dynastyId: string, kingId: string): King | undefined {
  const dynasty = getDynasty(dynastyId);
  return dynasty?.kings.find((k) => k.id === kingId);
}

export function getAllKings(): { king: King; dynasty: Dynasty }[] {
  return dynasties.flatMap((d) => d.kings.map((k) => ({ king: k, dynasty: d })));
}

export function searchKings(query: string): { king: King; dynasty: Dynasty; matchType: string }[] {
  const q = query.toLowerCase();
  const results: { king: King; dynasty: Dynasty; matchType: string }[] = [];

  for (const dynasty of dynasties) {
    for (const king of dynasty.kings) {
      if (king.name.toLowerCase().includes(q) || king.hanja?.toLowerCase().includes(q)) {
        results.push({ king, dynasty, matchType: "왕" });
      }
    }
  }
  return results;
}

export function searchEvents(query: string): { year: number; text: string; king: King; dynasty: Dynasty }[] {
  const q = query.toLowerCase();
  const results: { year: number; text: string; king: King; dynasty: Dynasty }[] = [];

  for (const dynasty of dynasties) {
    for (const king of dynasty.kings) {
      for (const event of king.events) {
        if (event.text.toLowerCase().includes(q) || event.year.toString().includes(q)) {
          results.push({ ...event, king, dynasty });
        }
      }
    }
  }
  return results;
}

export function searchPeople(query: string): { person: string; king: King; dynasty: Dynasty }[] {
  const q = query.toLowerCase();
  const results: { person: string; king: King; dynasty: Dynasty }[] = [];

  for (const dynasty of dynasties) {
    for (const king of dynasty.kings) {
      for (const person of king.people) {
        if (person.toLowerCase().includes(q)) {
          results.push({ person, king, dynasty });
        }
      }
    }
  }
  return results;
}
